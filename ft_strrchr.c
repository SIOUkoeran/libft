const char	*strrchr(const char *s, int c)
{
	int		i;
	char	*temp;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
		{
			temp = s + i;
		}
		i++;
	}
	return (temp);
}
